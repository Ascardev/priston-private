static CharacterClassData * GetCharacterClassData(int iClass, int iRank)
{
	int iCounter = _countof(saCharacterClassData);

	int iIndex = 0;

	for (int i = 0; i < iCounter; i++)
	{
		CharacterClassData * ps = saCharacterClassData + i;

		if (ps->iClass == iClass)
		{
			if (iRank == iIndex)
				return ps;

			iIndex++;
		}
	}

	return nullptr;
}

